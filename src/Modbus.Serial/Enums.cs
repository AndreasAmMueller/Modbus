namespace AMWD.Modbus.Serial
{
	/// <summary>
	/// Defines the baud rates for a serial connection.
	/// </summary>
	public enum BaudRate : int
	{
		/// <summary>
		/// 2400 Baud.
		/// </summary>
		Baud2400 = 2400,
		/// <summary>
		/// 4800 Baud.
		/// </summary>
		Baud4800 = 4800,
		/// <summary>
		/// 9600 Baud.
		/// </summary>
		Baud9600 = 9600,
		/// <summary>
		/// 19200 Baud.
		/// </summary>
		Baud19200 = 19200,
		/// <summary>
		/// 38400 Baud.
		/// </summary>
		Baud38400 = 38400,
		/// <summary>
		/// 57600 Baud.
		/// </summary>
		Baud57600 = 57600,
		/// <summary>
		/// 115200 Baud.
		/// </summary>
		Baud115200 = 115200
	}
}
